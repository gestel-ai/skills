def load_entries(ledger_path: Path) -> list[dict[str, Any]]:
    ledger = json.loads(ledger_path.read_text())
    entries = ledger.get("entries")
    if not isinstance(entries, list):
        raise ValueError(f"{ledger_path} must contain an entries array")
    return [entry for entry in entries if entry.get("status") == ACTIVE_STATUS]


def scaffold_entry(root: Path, entry: dict[str, Any]) -> str:
    local_skill = entry["local_skill"]
    if not re.fullmatch(r"[a-z0-9-]+", local_skill):
        raise ValueError(f"Invalid local skill name: {local_skill}")
    skill_dir = root / ".agents" / "skills" / local_skill
    (skill_dir / "references").mkdir(parents=True, exist_ok=True)
    (skill_dir / "evals").mkdir(parents=True, exist_ok=True)
    (skill_dir / "SKILL.md").write_text(skill_markdown(entry))
    (skill_dir / "references" / "source-usage.md").write_text(
        source_usage_markdown(entry)
    )
    (skill_dir / "references" / "provenance.md").write_text(provenance_markdown(entry))
    (skill_dir / "evals" / "evals.json").write_text(evals_json(entry))
    return local_skill


def scaffold_from_ledger(root: Path, ledger_path: Path) -> list[str]:
    return [scaffold_entry(root, entry) for entry in load_entries(ledger_path)]


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description="Scaffold active local skill migrations"
    )
    parser.add_argument(
        "--root",
        type=Path,
        default=ROOT,
        help="Repository root, defaulting to this script's project root",
    )
    parser.add_argument(
        "--ledger",
        type=Path,
        default=Path("docs/skill-migration-ledger.json"),
        help="Migration ledger path relative to root unless absolute",
    )
    args = parser.parse_args(argv)

    root = args.root.resolve()
    ledger = args.ledger if args.ledger.is_absolute() else root / args.ledger
    created = scaffold_from_ledger(root, ledger)
    for local_skill in created:
        print(f"Scaffolded {local_skill}")
    print(f"OK: scaffolded {len(created)} active skill migration(s)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
